# Fundamental Data Types: Numbers

# Integers
print(2 + 4)
print(type(2 + 4))

print(2 - 4)
print(type(2 - 4))

print(2 * 4)
print(type(2 * 4))

# Float
print(2 / 4)
print(type(2 / 4))

print(type(10.56))
print(type(20 + 1.1))

print(9.9 + 1.1)
print(type(9.9 + 1.1))

print(2**3)
print(6 // 4)
print(6 % 4)