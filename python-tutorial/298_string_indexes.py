# [start:stop:]
numbers = '01234567'
print(numbers[0:2])
print(numbers[0:7])
print(numbers[0:8])

# [start:stop:step_over]
print(numbers[0:8:2])

print(numbers[1:])
print(numbers[:5])
print(numbers[::1])

# Reverse
print(numbers[-1])
print(numbers[::-1])
print(numbers[::-2])
